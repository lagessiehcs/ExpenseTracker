#ifndef STATE_H
#define STATE_H

#include <unordered_map>
#include "../User.h"
#include "../Group.h"

enum class StateName
{
    INIT,

    START,
    CHOOSE_USER,
    CREATE_USER,

    USER_HOME,
    CREATE_GROUP,
    CHOOSE_GROUP,
    JOIN_GROUP,

    GROUP_HOME,
    MEMBER_LIST,
    ADD_EXPENSE,
    CHECK_EXPENSE,
    SETTLEMENT,
    LEAVE_GROUP,
    EDIT_EXPENSE,
    BALANCE,

    AUTO_GEN, // Autogenerated mode

    STOP
};

class State
{

protected:
    StateName _name;

    virtual void entry();

    virtual void during();

    // virtual void exit();

    static inline unsigned _user_id;
    static inline unsigned _group_id;

    static inline std::unordered_map<unsigned, User> _user_umap;
    static inline std::unordered_map<unsigned, Group> _group_umap;

public:
    State(StateName name);

    virtual StateName run();

    virtual void enter();

    virtual StateName transitions();
};

#endif
